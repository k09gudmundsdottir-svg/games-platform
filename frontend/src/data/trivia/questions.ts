/**
 * Shuffles an array in place using the Fisher-Yates algorithm.
 */
function shuffle<T>(arr: T[]): T[] {
  const result = [...arr];
  for (let i = result.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [result[i], result[j]] = [result[j], result[i]];
  }
  return result;
}

/**
 * Get filtered and shuffled trivia questions.
 *
 * @param opts.category - Filter by category (e.g., "Geography", "Science")
 * @param opts.difficulty - Filter by difficulty ("easy", "medium", "hard")
 * @param opts.count - Number of questions to return (default: 20)
 * @returns Shuffled array of trivia questions
 */
export function getQuestions(opts?: {
  category?: TriviaQuestion["category"];
  difficulty?: TriviaQuestion["difficulty"];
  count?: number;
}): TriviaQuestion[] {
  let filtered = questions;

  if (opts?.category) {
    filtered = filtered.filter((q) => q.category === opts.category);
  }

  if (opts?.difficulty) {
    filtered = filtered.filter((q) => q.difficulty === opts.difficulty);
  }

  const shuffled = shuffle(filtered);
  const count = opts?.count ?? 20;

  return shuffled.slice(0, count);
}
